QVector3D MarchingCubes::interpolateVertex(const QVector3D& p1, const QVector3D& p2,
                                             double v1, double v2, double isoValue)
{
    if (std::abs(v1 - v2) < 1e-15)
        return (p1 + p2) * 0.5f;

    double t = (isoValue - v1) / (v2 - v1);
    t = std::clamp(t, 0.0, 1.0);

    return p1 + (p2 - p1) * static_cast<float>(t);
}

QVector3D MarchingCubes::computeGradient(const data::Volume3D& volume,
                                           std::size_t ix, std::size_t iy, std::size_t iz)
{
    std::size_t nx = volume.sizeX();
    std::size_t ny = volume.sizeY();
    std::size_t nz = volume.sizeZ();

    // Central differences, clamped at boundaries.
    std::size_t xm = (ix > 0) ? ix - 1 : 0;
    std::size_t xp = (ix + 1 < nx) ? ix + 1 : nx - 1;
    std::size_t ym = (iy > 0) ? iy - 1 : 0;
    std::size_t yp = (iy + 1 < ny) ? iy + 1 : ny - 1;
    std::size_t zm = (iz > 0) ? iz - 1 : 0;
    std::size_t zp = (iz + 1 < nz) ? iz + 1 : nz - 1;

    double gx = volume.valueAt({xp, iy, iz}) - volume.valueAt({xm, iy, iz});
    double gy = volume.valueAt({ix, yp, iz}) - volume.valueAt({ix, ym, iz});
    double gz = volume.valueAt({ix, iy, zp}) - volume.valueAt({ix, iy, zm});

    QVector3D grad(static_cast<float>(-gx), static_cast<float>(-gy), static_cast<float>(-gz));
    float len = grad.length();
    if (len > 1e-7f)
        grad /= len;

    return grad;
}

MarchingCubes::Mesh MarchingCubes::extract(const data::Volume3D& volume, double isoValue)
{
    Mesh mesh;

    std::size_t nx = volume.sizeX();
    std::size_t ny = volume.sizeY();
    std::size_t nz = volume.sizeZ();

    if (nx < 2 || ny < 2 || nz < 2)
        return mesh;

    auto dims = volume.dimensions();
    const auto& dimX = dims[0];
    const auto& dimY = dims[1];
    const auto& dimZ = dims[2];

    // Process each cube in the volume.
    for (std::size_t iz = 0; iz < nz - 1; ++iz) {
        for (std::size_t iy = 0; iy < ny - 1; ++iy) {
            for (std::size_t ix = 0; ix < nx - 1; ++ix) {
                // Get the 8 corner values and positions.
                double val[8];
                QVector3D pos[8];

                // Cube vertex numbering:
                // 0: (ix,   iy,   iz)     4: (ix,   iy,   iz+1)
                // 1: (ix+1, iy,   iz)     5: (ix+1, iy,   iz+1)
                // 2: (ix+1, iy+1, iz)     6: (ix+1, iy+1, iz+1)
                // 3: (ix,   iy+1, iz)     7: (ix,   iy+1, iz+1)

                val[0] = volume.valueAt({ix, iy, iz});
                val[1] = volume.valueAt({ix + 1, iy, iz});
                val[2] = volume.valueAt({ix + 1, iy + 1, iz});
                val[3] = volume.valueAt({ix, iy + 1, iz});
                val[4] = volume.valueAt({ix, iy, iz + 1});
                val[5] = volume.valueAt({ix + 1, iy, iz + 1});
                val[6] = volume.valueAt({ix + 1, iy + 1, iz + 1});
                val[7] = volume.valueAt({ix, iy + 1, iz + 1});

                auto coord = [&](std::size_t x, std::size_t y, std::size_t z) {
                    return QVector3D(
                        static_cast<float>(dimX.coordinates.valueAt(x)),
                        static_cast<float>(dimY.coordinates.valueAt(y)),
                        static_cast<float>(dimZ.coordinates.valueAt(z)));
                };

                pos[0] = coord(ix, iy, iz);
                pos[1] = coord(ix + 1, iy, iz);
                pos[2] = coord(ix + 1, iy + 1, iz);
                pos[3] = coord(ix, iy + 1, iz);
                pos[4] = coord(ix, iy, iz + 1);
                pos[5] = coord(ix + 1, iy, iz + 1);
                pos[6] = coord(ix + 1, iy + 1, iz + 1);
                pos[7] = coord(ix, iy + 1, iz + 1);

                // Determine cube index from corner values vs iso value.
                uint8_t cubeIndex = 0;
                for (int c = 0; c < 8; ++c) {
                    if (val[c] < isoValue)
                        cubeIndex = static_cast<uint8_t>(cubeIndex | (1u << c));
                }

                if (kEdgeTable[cubeIndex] == 0)
                    continue;

                // Interpolate edge vertices.
                QVector3D edgeVerts[12];

                if (kEdgeTable[cubeIndex] & 0x001)
                    edgeVerts[0] = interpolateVertex(pos[0], pos[1], val[0], val[1], isoValue);
                if (kEdgeTable[cubeIndex] & 0x002)
                    edgeVerts[1] = interpolateVertex(pos[1], pos[2], val[1], val[2], isoValue);
                if (kEdgeTable[cubeIndex] & 0x004)
                    edgeVerts[2] = interpolateVertex(pos[2], pos[3], val[2], val[3], isoValue);
                if (kEdgeTable[cubeIndex] & 0x008)
                    edgeVerts[3] = interpolateVertex(pos[3], pos[0], val[3], val[0], isoValue);
                if (kEdgeTable[cubeIndex] & 0x010)
                    edgeVerts[4] = interpolateVertex(pos[4], pos[5], val[4], val[5], isoValue);
                if (kEdgeTable[cubeIndex] & 0x020)
                    edgeVerts[5] = interpolateVertex(pos[5], pos[6], val[5], val[6], isoValue);
                if (kEdgeTable[cubeIndex] & 0x040)
                    edgeVerts[6] = interpolateVertex(pos[6], pos[7], val[6], val[7], isoValue);
                if (kEdgeTable[cubeIndex] & 0x080)
                    edgeVerts[7] = interpolateVertex(pos[7], pos[4], val[7], val[4], isoValue);
                if (kEdgeTable[cubeIndex] & 0x100)
                    edgeVerts[8] = interpolateVertex(pos[0], pos[4], val[0], val[4], isoValue);
                if (kEdgeTable[cubeIndex] & 0x200)
                    edgeVerts[9] = interpolateVertex(pos[1], pos[5], val[1], val[5], isoValue);
                if (kEdgeTable[cubeIndex] & 0x400)
                    edgeVerts[10] = interpolateVertex(pos[2], pos[6], val[2], val[6], isoValue);
                if (kEdgeTable[cubeIndex] & 0x800)
                    edgeVerts[11] = interpolateVertex(pos[3], pos[7], val[3], val[7], isoValue);

                // Generate triangles from the triangle table.
                for (int t = 0; kTriTable[cubeIndex][t] != -1; t += 3) {
                    uint32_t baseIdx = static_cast<uint32_t>(mesh.vertices.size());

                    for (int v = 0; v < 3; ++v) {
                        int edgeIdx = kTriTable[cubeIndex][t + v];
                        Vertex vert;
                        vert.position = edgeVerts[edgeIdx];
                        // Approximate normal from gradient at the nearest grid vertex.
                        vert.normal = computeGradient(volume, ix, iy, iz);
                        mesh.vertices.push_back(vert);
                        mesh.indices.push_back(baseIdx + static_cast<uint32_t>(v));
                    }
                }
            }
        }
    }

    // Post-process: compute face normals for better quality.
    for (std::size_t i = 0; i + 2 < mesh.indices.size(); i += 3) {
        const QVector3D& v0 = mesh.vertices[mesh.indices[i]].position;
        const QVector3D& v1 = mesh.vertices[mesh.indices[i + 1]].position;
        const QVector3D& v2 = mesh.vertices[mesh.indices[i + 2]].position;

        QVector3D faceNormal = QVector3D::crossProduct(v1 - v0, v2 - v0).normalized();
        if (faceNormal.length() < 0.5f)
            faceNormal = QVector3D(0, 0, 1);

        mesh.vertices[mesh.indices[i]].normal = faceNormal;
        mesh.vertices[mesh.indices[i + 1]].normal = faceNormal;
        mesh.vertices[mesh.indices[i + 2]].normal = faceNormal;
    }

    return mesh;
}

